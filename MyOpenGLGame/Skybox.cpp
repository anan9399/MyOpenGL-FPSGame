Skybox::Skybox()
{
}

Skybox::~Skybox()
{
	glDeleteVertexArrays(1, &VAO);
	glDeleteBuffers(1, &VBO);
}

void Skybox::InitSkybox() {
	m_shader = new Shader("skyboxvert.vert", "skyboxfrag.frag");

	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);

	glGenBuffers(1, &VBO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);

	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

	m_texture = Texture::GetInstance().loadSkyboxRes(faces);

}

void Skybox::draw(Camera camera, int Screen_Width , int Screen_Height )
{
	glm::mat4 view;
	glm::mat4 projection;


	glDepthFunc(GL_LEQUAL);  
	
	m_shader->use();
	view = glm::mat4(glm::mat3(camera.GetViewMatrix()));
	projection = glm::perspective(glm::radians(camera.Zoom), (float)Screen_Width / (float)Screen_Height, 0.1f, 100.0f);
	m_shader->setUniform4m("view", view);
	m_shader->setUniform4m("projection", projection);

	// skybox cube
	glBindVertexArray(VAO);
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_CUBE_MAP, m_texture);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
	glDepthFunc(GL_LESS);


}
